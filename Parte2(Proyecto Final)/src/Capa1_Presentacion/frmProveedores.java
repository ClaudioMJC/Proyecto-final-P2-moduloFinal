    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        frmBuscarProveedor frmBuscar = new frmBuscarProveedor(null, true);
        frmBuscar.addWindowListener(new WindowAdapter() {

            @Override
            public void windowClosed(WindowEvent e) {

                try {
                    int id = frmBuscar.ObtnerId();
                    String condicion = "";
                    LNProveedor logica = new LNProveedor();
                    Proveedor proveedor;
                    if (id > -1) {
                        condicion = String.format("ID_PROVEEDOR = %d", id);
                        proveedor = logica.ObtenerRegistro(condicion);
                        txtId.setText(String.valueOf(proveedor.getIdProveedor()));
                        txtNombre.setText(proveedor.getNombre());
                        txtApellido.setText(proveedor.getApellido());
                        txtTelefono.setText(proveedor.getTelefono());
                        txtDescripcion.setText(proveedor.getDescripcion());

                    } else {

                        txtId.setText("");
                        txtNombre.setText("");
                        txtApellido.setText("");
                        txtTelefono.setText("");
                        txtDescripcion.setText("");
                    }
                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(null, ex.getMessage());
                }
            }

        });

        frmBuscar.setVisible(true);
    }//GEN-LAST:event_btnBuscarActionPerformed

