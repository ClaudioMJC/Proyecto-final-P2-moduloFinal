    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        LNProveedor logica = new LNProveedor();
        Proveedor proveedor = GenerarEntidad();

        try {
            if (proveedor.isExiste()) {
                logica.Modificar(proveedor);
            } else {
                logica.Insertar(proveedor);
            }
            JOptionPane.showMessageDialog(this, logica.getMensaje());
            Limpiar();
            CargarDatos("");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

    
    
    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        LNProveedor logica = new LNProveedor();
        Proveedor proveedor;
        try {
            proveedor = GenerarEntidad();
            if (proveedor.isExiste()) {
                if (logica.Eliminar(proveedor) > 0) {
                    JOptionPane.showMessageDialog(this, logica.getMensaje());
                    Limpiar();
                    CargarDatos("");
                } else {
                    JOptionPane.showMessageDialog(this, "No fue posible eliminar el proveedor");
                }
            } else {
                JOptionPane.showMessageDialog(this, "Se debe seleccionar al proveedor que desea eliminar");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void tblProveedoresMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblProveedoresMouseClicked
        try {
            LNProveedor logica = new LNProveedor();
            Proveedor proveedor;
            String condicion;
            if (evt.getClickCount() == 2) {
                int fila = tblProveedores.rowAtPoint(evt.getPoint());
                txtId.setText(tblProveedores.getValueAt(fila, 0).toString());
                condicion = String.format("ID_PROVEEDOR=%s", txtId.getText());
                proveedor = logica.ObtenerRegistro(condicion);
                txtId.setText(String.valueOf(proveedor.getIdProveedor()));
                txtNombre.setText(proveedor.getNombre());
                txtApellido.setText(proveedor.getApellido());
                txtTelefono.setText(proveedor.getTelefono());
                txtDescripcion.setText(proveedor.getDescripcion());
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_tblProveedoresMouseClicked


