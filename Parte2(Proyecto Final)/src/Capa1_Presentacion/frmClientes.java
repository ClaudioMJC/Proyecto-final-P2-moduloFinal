    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        frmBuscarCliente frmbuscar = new frmBuscarCliente(null,true);
        frmbuscar.addWindowListener(new WindowAdapter() {

            @Override
            public void windowClosed(WindowEvent e) {

                try {
                    int id = frmbuscar.ObtnerId();
                    String condicion = "";
                    LNCliente logica = new LNCliente();
                    Cliente cliente;
                    if (id > -1) {
                        condicion = String.format("id_cliente = %d", id);
                        cliente = logica.ObtenerRegistro(condicion);
                        txtId.setText(String.valueOf(cliente.getIdCliente()));
                        txtNombre.setText(cliente.getNombre());
                        txtApellido.setText(cliente.getApellido());
                        txtDireccion.setText(cliente.getDireccion());
                        txtTelefono.setText(cliente.getTelefono());
                        

                    } else {

                        txtId.setText("");
                        txtNombre.setText("");
                        txtApellido.setText("");
                        txtDireccion.setText("");
                        txtTelefono.setText("");
                        
                    }
                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(null, ex.getMessage());
                }
            }

        });

        frmbuscar.setVisible(true);
    }//GEN-LAST:event_btnBuscarActionPerformed

