    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        LNCliente logica = new LNCliente();
        Cliente cliente;
        try {
            cliente=GenerarEntidad();
            if (cliente.isExiste()) {
                if (logica.Eliminar(cliente)>0) {
                    JOptionPane.showMessageDialog(this,logica.getMensaje());
                    Limpiar();

                    CargarDatos("");

                }else{
                    JOptionPane.showMessageDialog(this, "No fue posible eliminar el cliente");

                }

            }else{
                JOptionPane.showMessageDialog(this, "Se debe seleccionar al cliente que desea eliminar");

            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

